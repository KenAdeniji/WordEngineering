	///<summary>
	/// 2019-04-28 	Created.
	///</summary>
	public class Rectangle
	{
		public bool IsSquare(Rectangle rectangle)
		{  
			return (rectangle.Height == rectangle.Width);
		}
		
		public decimal Height { get; set; }
		public decimal Width { get; set; }
	}
}
