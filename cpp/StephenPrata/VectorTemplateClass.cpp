#include <iostream>
#include <string>
#include <vector>

using namespace std;

int main()
{
	vector<string> pentateuch =
	{
		"Genesis",
		"Exodus",
		"Leviticus",
		"Numbers",
		"Deuteronomy"
	}
	;

	for 
	(
		int index = 0;
		index < pentateuch.size();
		++index
	)
	{
		cout << pentateuch[index] << endl;
	}	
}
